		$default = array(
			'A1_FILIAL'  => '01',
			'A1_COD'     => '', // Generated by Protheus
			'A1_LOJA'    => '01',
			'A1_NOME'    => 'billing_first_name + billing_last_name',
			'A1_NREDUZ'  => 'billing_first_name',
			'A1_CGC'     => 'billing_cpf|billing_cnpj',
			'A1_TIPO'    => 'auto', // F or J based on document length
			'A1_END'     => 'billing_address_1',
			'A1_BAIRRO'  => 'billing_neighborhood',
			'A1_MUN'     => 'billing_city',
			'A1_EST'     => 'billing_state',
			'A1_CEP'     => 'billing_postcode',
			'A1_DDD'     => 'billing_phone',
			'A1_TEL'     => 'billing_phone',
			'A1_EMAIL'   => 'billing_email',
		);

		return get_option( self::OPTION_PREFIX . 'customer_mapping', $default );
	}

