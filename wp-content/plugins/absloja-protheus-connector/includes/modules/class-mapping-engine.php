/**
 * Mapping_Engine class
 *
 * Handles configuration and retrieval of field mappings between WooCommerce and Protheus.
 */
class Mapping_Engine {

	/**
	 * Option prefix for storing mappings
	 *
	 * @var string
	 */
	private const OPTION_PREFIX = 'absloja_protheus_';

	/**
	 * Constructor
	 */
	public function __construct() {
		// Initialize default mappings if not set
		$this->maybe_initialize_defaults();
	}

	/**
	 * Get customer field mapping (WooCommerce → Protheus SA1)
	 *
	 * @return array Customer field mapping configuration
	 */
