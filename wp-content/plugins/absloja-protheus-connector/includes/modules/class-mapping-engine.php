		$default = array(
			'pending'   => 'pending',
			'approved'  => 'processing',
			'invoiced'  => 'completed',
			'shipped'   => 'completed',
			'cancelled' => 'cancelled',
			'rejected'  => 'failed',
		);

		$mapping = get_option( self::OPTION_PREFIX . 'status_mapping', $default );

		return $mapping[ $protheus_status ] ?? 'pending';
	}

	/**
	 * Validate mapping configuration
	 *
	 * @param string $type Mapping type (customer, order, product, payment, category, tes, status)
	 * @param array  $mapping Mapping configuration to validate
	 * @return array Array of validation errors (empty if valid)
	 */
	public function validate_mapping( string $type, array $mapping ): array {
		$errors = array();

		switch ( $type ) {
			case 'customer':
				$required_fields = array( 'A1_FILIAL', 'A1_NOME', 'A1_CGC', 'A1_END', 'A1_MUN', 'A1_EST' );
				foreach ( $required_fields as $field ) {
					if ( ! isset( $mapping[ $field ] ) || empty( $mapping[ $field ] ) ) {
						$errors[] = sprintf( 'Required field %s is missing or empty', $field );
					}
				}
				break;

			case 'order':
				if ( ! isset( $mapping['SC5'] ) || ! is_array( $mapping['SC5'] ) ) {
					$errors[] = 'SC5 mapping is required';
				}
				if ( ! isset( $mapping['SC6'] ) || ! is_array( $mapping['SC6'] ) ) {
					$errors[] = 'SC6 mapping is required';
				}
				break;

			case 'product':
				$required_fields = array( 'sku', 'name', 'regular_price' );
				foreach ( $required_fields as $field ) {
					if ( ! isset( $mapping[ $field ] ) || empty( $mapping[ $field ] ) ) {
						$errors[] = sprintf( 'Required field %s is missing or empty', $field );
					}
				}
				break;

			case 'payment':
			case 'category':
			case 'tes':
			case 'status':
				if ( ! is_array( $mapping ) ) {
					$errors[] = 'Mapping must be an array';
				}
				break;

			default:
				$errors[] = 'Invalid mapping type';
		}

		return $errors;
	}

	/**
	 * Save mapping configuration
	 *
	 * @param string $type Mapping type
	 * @param array  $mapping Mapping configuration
	 * @return bool True on success, false on failure
	 */
	public function save_mapping( string $type, array $mapping ): bool {
		$errors = $this->validate_mapping( $type, $mapping );
		if ( ! empty( $errors ) ) {
			return false;
		}

		$option_name = self::OPTION_PREFIX . $type . '_mapping';
		if ( 'tes' === $type ) {
			$option_name = self::OPTION_PREFIX . 'tes_rules';
		}

		return update_option( $option_name, $mapping );
	}

