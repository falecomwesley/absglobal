	/**
	 * Get payment method mapping (WooCommerce → Protheus payment conditions)
	 *
	 * @param string $woo_method WooCommerce payment method ID
	 * @return string|null Protheus payment condition code or null if not mapped
	 */
	public function get_payment_mapping( string $woo_method ): ?string {
		$default = array(
			'bacs'        => '001', // Bank transfer
			'cheque'      => '002', // Check
			'cod'         => '003', // Cash on delivery
			'credit_card' => '004', // Credit card
			'pix'         => '005', // PIX
		);

		$mapping = get_option( self::OPTION_PREFIX . 'payment_mapping', $default );

		return $mapping[ $woo_method ] ?? $mapping['cod'] ?? '003';
	}

